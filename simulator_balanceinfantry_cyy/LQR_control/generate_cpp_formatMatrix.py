def generate_cpp_code(input_string):
    input_lines = input_string.strip().split('\n')
    matrix_values = [list(map(float, line.split())) for line in input_lines]
    
    code = "double LQR_K[][] = {\n"
    
    for row in matrix_values:
        code += "    {"
        code += ", ".join([f"{elem:.10f}" for elem in row])
        code += "},\n"
    
    code += "};\n"
    
    return code

